-- Migracao para suporte multi-usuario
-- Atribui todos os dados existentes ao usuario "admin"
-- Pre-requisito: usuario com username='admin' deve existir na tabela `users`

SET @adminId = (SELECT id FROM users WHERE username = 'admin' LIMIT 1);

-- Aborta a execucao se nao houver usuario admin
SELECT IF(@adminId IS NULL,
          (SELECT CONCAT('ERRO: usuario admin nao encontrado em users. Crie o admin antes de executar esta migration.')
           FROM information_schema.tables WHERE 1=0),
          'OK') AS preflight;

-- ATIVO_CARTEIRA
ALTER TABLE ATIVO_CARTEIRA ADD COLUMN ID_USUARIO BIGINT NULL;
UPDATE ATIVO_CARTEIRA SET ID_USUARIO = @adminId WHERE ID_USUARIO IS NULL;
ALTER TABLE ATIVO_CARTEIRA MODIFY COLUMN ID_USUARIO BIGINT NOT NULL;
ALTER TABLE ATIVO_CARTEIRA ADD CONSTRAINT fk_ativo_carteira_user FOREIGN KEY (ID_USUARIO) REFERENCES users(id);

