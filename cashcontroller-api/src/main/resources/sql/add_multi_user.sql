-- ATIVO: NULL = renda variavel (compartilhado); preenchido = renda fixa do usuario
ALTER TABLE ATIVO ADD COLUMN ID_USUARIO BIGINT NULL;
UPDATE ATIVO a
   SET a.ID_USUARIO = @adminId
 WHERE a.ID_USUARIO IS NULL
   AND EXISTS (SELECT 1 FROM PARAMETRO_RENDA_FIXA p WHERE p.ID_ATIVO = a.ID_ATIVO);
ALTER TABLE ATIVO ADD CONSTRAINT fk_ativo_user FOREIGN KEY (ID_USUARIO) REFERENCES users(id);
